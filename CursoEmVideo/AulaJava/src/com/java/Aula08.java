package com.java;

import java.util.Scanner;

public class Aula08 {
    public static void main(String[] args) {
        Scanner in = new Scanner(System.in);

        int valorA, valorB;
        valorA = 5;
        valorB = 3;
        int resultado;

        resultado = valorA + valorB;
        System.out.printf("%d + %d = %d\n", valorA, valorB, resultado);

        resultado = valorA / valorB;
        System.out.printf("%d / %d = %d\n", valorA, valorB, resultado);

        resultado = valorA * valorB;
        System.out.printf("%d * %d = %d\n", valorA, valorB, resultado);

        resultado = valorA - valorB;
        System.out.printf("%d - %d = %d\n", valorA, valorB, resultado);






    }
}
